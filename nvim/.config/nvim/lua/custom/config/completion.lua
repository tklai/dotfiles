local icons = {
  Text = "",
  Method = "",
  Function = "",
  Constructor = "⌘",
  Field = "ﰠ",
  Variable = "",
  Class = "ﴯ",
  Interface = "",
  Module = "",
  Property = "ﰠ",
  Unit = "祐",
  Value = "",
  Enum = "",
  Keyword = "",
  Snippet = "",
  Color = "",
